#include <stdlib.h>
#include <math.h>
#include "pnglite.h"

#include <nexrad/image.h>

static size_t _image_size(size_t width, size_t height) {
    return NEXRAD_IMAGE_PIXEL_BYTES * width * height;
}

nexrad_image *nexrad_image_create(size_t width, size_t height) {
    nexrad_image *image;
    size_t size;
    uint8_t *buf;

    if (width == 0 || height == 0) {
        return NULL;
    }

    if ((image = malloc(sizeof(nexrad_image))) == NULL) {
        goto error_malloc_image;
    }

    size = _image_size(width, height);

    if ((buf = malloc(size)) == NULL) {
        goto error_malloc_buf;
    }

    memset(buf, '\0', size);

    image->buf    = buf;
    image->size   = size;
    image->width  = width;
    image->height = height;

    return image;

error_malloc_buf:
    free(image);

error_malloc_image:
    return NULL;
}

int nexrad_image_save_png(nexrad_image *image, const char *path) {
    png_t png;
    
    if (image == NULL || path == NULL) {
        return -1;
    }

    memset(&png, '\0', sizeof(png));

    png_init(NULL, NULL);

    if (png_open_file_write(&png, path) < 0) {
        goto error_open_file_write;
    }

    if (png_set_data(&png,
        image->width, image->height, NEXRAD_IMAGE_COLOR_DEPTH, NEXRAD_IMAGE_COLOR_FORMAT, image->buf
    ) < 0) {
        goto error_set_data;
    }

    if (png_close_file(&png) < 0) {
        goto error_close_file;
    }

    return 0;

error_close_file:
    return -1;

error_set_data:
    png_close_file(&png);

error_open_file_write:
    return -1;
}

void nexrad_image_destroy(nexrad_image *image) {
    if (image == NULL) {
        return;
    }

    if (image->buf) {
        free(image->buf);
    }

    free(image);
}
